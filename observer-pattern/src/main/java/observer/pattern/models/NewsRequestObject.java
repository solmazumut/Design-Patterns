package observer.pattern.models;

public class NewsRequestObject {
    String type;
    String text;

    public NewsRequestObject() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}


